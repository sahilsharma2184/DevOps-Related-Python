#Assignment operators are used to assign values to variables:

# '=' operator assigns value to a variable
a=5 
print(a) #o/p: 5


# 'a+=1' operator means a=a+1
a+=1 #a=a+1
print(a) #o/p: 6


# 'a-=1' operator means a=a+1
b=5
b-=1 #b=b-1
print(b) #o/p: 4


# 'c*=2' operator means c=c*2
c=5
c*=2
print(c) #o/p: 10


# 'd/=2' operator means d=d/2
d=10
d/=2
print(d) #o/p: 5.0


# 'e%=3' operator means e=e%3
e=14
e%=3
print(e) #o/p: 2