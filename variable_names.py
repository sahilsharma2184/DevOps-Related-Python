#Variable names are case sensitive

#Camel Case Variable Naming Technique
myVariableName="Sahil"
print(myVariableName) #o/p: Sahil

#Pascal Case Variable Naming Technique
MyVariableName="Sharma" #o/p: Sharma
print(MyVariableName)

#Snake Case Variable Naming Technique
my_variable_name="SRM AP" #o/p: SRM AP
print(my_variable_name)